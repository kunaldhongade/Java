public class _03_Literals {
    public static void main(String[] args){
        // Literals means value stored in variable
         byte age = 12;
         char ch = 'A';
         float f = 123.532f;
         double d = 1234.6343d;
         long l = 12342343223423l;
         System.out.println(age);
         System.out.println(ch);
         System.out.println(f);
         System.out.println(d);
         System.out.println(l);
         System.out.println("Java is fucking language with protection");
    }
}

